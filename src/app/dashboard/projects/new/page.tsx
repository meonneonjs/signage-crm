import { prisma } from '@/lib/db';
import Link from 'next/link';

async function getClients() {
  try {
    return await prisma.client.findMany({
      orderBy: {
        name: 'asc'
      }
    });
  } catch (error) {
    console.error('Error fetching clients:', error);
    return [];
  }
}

